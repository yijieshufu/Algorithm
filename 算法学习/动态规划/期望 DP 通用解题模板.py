import sys

# 【骨架：正序 DP，滚动变量优化】
it = iter(sys.stdin.read().split())
try:
    n = int(next(it))
    s = next(it)
except StopIteration:
    pass

MOD = 10**9 + 7
inv2 = (MOD + 1) // 2  # 50% 概率必备：除以 2 的逆元

ans = 0 # 记录总期望分数
l1 = 0  # 记录以当前结尾的 E[L] (若是立方和，再加个 l2 = 0)

for c in s:
    if c == '1': 
        # ==========================================
        # 【填空区 A：确定发生时的增量】
        # 草稿纸推导: Delta = (L+1)^2 - L^2 = 2L + 1
        delta = (2 * l1 + 1) % MOD
        # 核心顺序：先用旧 l1 更新 ans，再更新 l1
        ans = (ans + delta) % MOD
        l1 = (l1 + 1) % MOD
        # ==========================================
        
    elif c == '0':
        # 确定中断，长度归零，总分不变
        l1 = 0
        
    else: # c == '?' 
        # ==========================================
        # 【填空区 B：随机发生时的加权转移】
        # 1. 计算若是 1 产生的增量，乘以概率 (1/2)
        delta = (2 * l1 + 1) % MOD
        ans = (ans + delta * inv2) % MOD 
        
        # 2. 状态值本身也要乘以概率
        l1 = (l1 + 1) * inv2 % MOD
        # ==========================================

print(ans)
